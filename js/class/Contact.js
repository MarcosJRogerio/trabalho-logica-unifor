class Contact {
  nome;
  email;
  telefone;
  endereco;
  dataDeNascimento;
  profissao;
  linkedin;

  constructor(nome, email, telefone, endereco, dataDeNascimento, profissao, linkedin) {
    this.nome = nome;
    this.email = email;
    this.telefone = telefone;
    this.endereco = endereco;
    this.dataDeNascimento = dataDeNascimento;
    this.profissao = profissao;
    this.linkedin = linkedin;
  }
}